// ================================================================
//  LOGIN SCREEN  —  Save session to SharedPreferences on success
// ================================================================
//
//  In your login_screen.dart, after a successful Firebase sign-in,
//  call this helper to persist the session:
//
//    await SessionManager.saveSession();
//
// ================================================================
abstract class SessionManager {
  /// Call this immediately after a successful Firebase sign-in.
  static Future<void> saveSession() async {
    final prefs = await SharedPreferences.getInstance();
    await prefs.setBool('isLoggedIn', true);
    // Optionally persist UID for quick lookups:
    final uid = FirebaseAuth.instance.currentUser?.uid;
    if (uid != null) await prefs.setString('uid', uid);
  }

  /// Call this on logout (home_screen.dart already calls prefs.clear()).
  static Future<void> clearSession() async {
    final prefs = await SharedPreferences.getInstance();
    await prefs.clear();
  }
}