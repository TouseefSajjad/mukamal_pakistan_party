// ================================================================
Future<void> main() async {
  WidgetsFlutterBinding.ensureInitialized();

  // ── 1. Firebase init ──────────────────────────────────────────
  await Firebase.initializeApp(
    options: DefaultFirebaseOptions.currentPlatform,
  );

  // ── 2. System UI overlay style ────────────────────────────────
  SystemChrome.setSystemUIOverlayStyle(const SystemUiOverlayStyle(
    statusBarColor: Colors.transparent,
    statusBarBrightness: Brightness.dark,
    statusBarIconBrightness: Brightness.light,
  ));

  // ── 3. Determine initial route from SharedPreferences ─────────
  final Widget homeWidget = await _resolveStartScreen();

  runApp(MukammalPakistanPartyApp(home: homeWidget));
}

// ── Decide whether to show Login or Home ─────────────────────
Future<Widget> _resolveStartScreen() async {
  // Check SharedPreferences first (fast, local)
  final prefs = await SharedPreferences.getInstance();
  final bool savedLoggedIn = prefs.getBool('isLoggedIn') ?? false;

  // Also verify Firebase still has a valid session (auth truth source)
  final firebaseUser = FirebaseAuth.instance.currentUser;

  if (savedLoggedIn && firebaseUser != null) {
    // Both checks pass → go directly to Home
    return const MainNavScreen();
  }

  // Mismatch or no session → clear prefs and show Login
  await prefs.clear();
  return const LoginScreen();
}

